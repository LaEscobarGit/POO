    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        int fila = tablaLista.getSelectedRow();
        if(fila != -1){
            String id = tablaLista.getValueAt(fila, 0).toString();
            datosSistema.setTxtProducto(id);
            JOptionPane.showMessageDialog(null, "Producto agregado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona una fila.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_agregarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String nom = nomField.getText().trim();
        Producto producto = null;
        if(nomField.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Escriba un nombre para buscar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                ArrayList<Producto> lista = inventario.getLista();
                for(Producto p: lista){
                    if(nom.equals(p.getNombre())){
                        int id = p.getId();
                        producto = inventario.buscarProducto(id);
                        break;
                    }
                }

                DefaultTableModel modelo = (DefaultTableModel) tablaLista.getModel();
                modelo.setRowCount(0);
                if (producto != null){
                    modelo.addRow(new Object[]{
                        producto.getId(), producto.getNombre(), producto.getCategoria(), producto.getTipo(), producto.getPrecio()
                    });
                }else{
                    JOptionPane.showMessageDialog(this, "Producto no encontrado.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Ingrese un nombre v√°lido.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }finally{
                nomField.setText("");
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

